#include <stdio.h>

int main()
{
    printf("0x%02x\n", 0xF1 & 0x1);
    printf("0x%02x\n", 0xF1 & 0x1 << 3);
    printf("0x%02x\n", 0xF1 & (0x1 << 3));
    printf("0x%02x\n", (0xF1 & 0x1) << 3);
    printf("0x%02x\n", 0xF1 && 0x1);
    printf("0x%02x\n", 0xF1 | 0x1);
    printf("0x%02x\n", 0xF1 || 0x1);
    printf("0x%02x\n", 0xF1 ^ 0x1);

    return 0;
}
